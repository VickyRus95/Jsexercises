function lessThanOrEqualToZero (num){
if (num <= 0){
    document.write ("TRUE")
}
else {document.write ("FALSE")
}
}
document.write ("<br>")
lessThanOrEqualToZero(8)
document.write ("<br>")
lessThanOrEqualToZero(-1)
document.write ("<br>")
lessThanOrEqualToZero(3)
document.write ("<br>")
lessThanOrEqualToZero(0)